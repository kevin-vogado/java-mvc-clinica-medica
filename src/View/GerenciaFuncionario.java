    private void b2_editar_funcionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b2_editar_funcionarioActionPerformed
        
        try {
            
            int id = 0; 
            String nome = "";
            String telefone = "";
            String cpf = "";
            String rua = "";
            int numero = 0; 
            String cep = "";
            String ref = ""; 
            
            if (this.c2_nome_funcionario.getText().length() < 2) { 
                throw new Mensagens("Nome deve conter ao menos 2 caracteres.");
            } else {
                nome = this.c2_nome_funcionario.getText(); 
            } 
            
            if (this.c2_telef_funcionario.getText().length() < 2) { 
                throw new Mensagens("Telefone deve conter ao menos 2 numeros.");
            } else {
                telefone = this.c2_telef_funcionario.getText(); 
            }
            
            if (this.c2_cpf_funcionario.getText().length() < 2) { 
                throw new Mensagens("CPF deve conter ao menos 2 caracteres.");
            } else {
                cpf = this.c2_cpf_funcionario.getText(); 
            }
            
            if (this.c2_rua_funcionario.getText().length() < 2) { 
                throw new Mensagens("Rua deve conter ao menos 2 caracteres.");
            } else {
                rua = this.c2_rua_funcionario.getText(); 
            }
            
            if (this.c2_num_funcionario.getText().length() <= 0) {
                throw new Mensagens("Número deve ser um número e maior que zero.");
            } else {
                numero = Integer.parseInt(this.c2_num_funcionario.getText());
            } 
            
            if (this.c2_cep_funcionario.getText().length() < 2) { 
                throw new Mensagens("CEP deve conter ao menos 2 caracteres.");
            } else {
                cep = this.c2_cep_funcionario.getText(); 
            } 
            
            if (this.c2_ref_funcionario.getText().length() < 2) { 
                throw new Mensagens("Nome deve conter ao menos 2 caracteres.");
            } else {
                ref = this.c2_ref_funcionario.getText(); 
            } 
            
            if (this.TableFuncionarios.getSelectedRow() == -1) {
                throw new Mensagens("Primeiro selecione um funcionário para editar!"); 
            } else {
                id = Integer.parseInt(this.TableFuncionarios.getValueAt(this.TableFuncionarios.getSelectedRow(), 0).toString()); 
            }
            
            if (this.controlador.Editar(cpf, rua, numero, cep, ref, id, nome, telefone)) {
                
                this.c2_nome_funcionario.setText(""); 
                this.c2_telef_funcionario.setText(""); 
                this.c2_cpf_funcionario.setText(""); 
                this.c2_rua_funcionario.setText(""); 
                this.c2_num_funcionario.setText(""); 
                this.c2_cep_funcionario.setText(""); 
                this.c2_ref_funcionario.setText(""); 
                JOptionPane.showMessageDialog(rootPane, "Funcionário editado com sucesso!"); 

            } 
            
            System.out.println(this.controlador.getMinhaLista().toString()); 
            
        } catch (Mensagens erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage());
        } catch (NumberFormatException erro2) {
            JOptionPane.showMessageDialog(null, "Informe um número.");
        } finally {
            carregaTabela(); 
        } 
    }//GEN-LAST:event_b2_editar_funcionarioActionPerformed

    private void b2_apagar_funcionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b2_apagar_funcionarioActionPerformed
        
            try {

            int id = 0;
            if (this.TableFuncionarios.getSelectedRow() == -1) {
                throw new Mensagens("Primeiro selecione um funcionário para APAGAR");
            } else {
                id = Integer.parseInt(this.TableFuncionarios.getValueAt(this.TableFuncionarios.getSelectedRow(), 0).toString());
            }

            int resposta_usuario = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja APAGAR este funcionário ?");

            if (resposta_usuario == 0) { 

                if (this.controlador.Apagar(id)) { 

                this.c2_nome_funcionario.setText(""); 
                this.c2_telef_funcionario.setText(""); 
                this.c2_cpf_funcionario.setText(""); 
                this.c2_rua_funcionario.setText(""); 
                this.c2_num_funcionario.setText(""); 
                this.c2_cep_funcionario.setText(""); 
                this.c2_ref_funcionario.setText(""); 
                JOptionPane.showMessageDialog(rootPane, "Funcionário apagado com sucesso!"); 

                }

            }

            System.out.println(this.controlador.getMinhaLista().toString());

        } catch (Mensagens erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage());
        } finally { 
            carregaTabela();
        }
    }//GEN-LAST:event_b2_apagar_funcionarioActionPerformed

